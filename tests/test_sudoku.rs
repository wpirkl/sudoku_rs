use sudoku::sudoku::Sudoku;


#[test]
fn test_sudoku_validation_valid()
{
    let board: [[u32; 9]; 9] = [
         [5, 3, 0, 0, 7, 0, 0, 0, 0]
        ,[6, 0, 0, 1, 9, 5, 0, 0, 0]
        ,[0, 9, 8, 0, 0, 0, 0, 6, 0]
        ,[8, 0, 0, 0, 6, 0, 0, 0, 3]
        ,[4, 0, 0, 8, 0, 3, 0, 0, 1]
        ,[7, 0, 0, 0, 2, 0, 0, 0, 6]
        ,[0, 6, 0, 0, 0, 0, 2, 8, 0]
        ,[0, 0, 0, 4, 1, 9, 0, 0, 5]
        ,[0, 0, 0, 0, 8, 0, 0, 7, 9]];

    let sudoku = Sudoku::<9,9>::new(board);
    assert!(sudoku.is_valid());
}


#[test]
fn test_sudoku_validation_not_valid()
{
    let board = [
        [8, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ];

    let sudoku = Sudoku::<9,9>::new(board);
    assert!(!sudoku.is_valid());
}

